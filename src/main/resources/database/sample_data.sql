-- Insert kết quả xét tốt nghiệp cho học sinh khối 12 (tự động tính toán)
INSERT INTO HocSinh_KetQuaTotNghiep (HS_ID, DiemXetTotNghiep, KetQua, GhiChu) 
SELECT 
    hs.ID,
    (((dt.TongDiemThi + ISNULL(dhk.DiemTBCacNam,0))/2.0) + ISNULL(dt.DiemUT,0)) AS DiemXetTotNghiep,
    CASE 
        WHEN dt.DiemToan > 1.0 AND dt.DiemVan > 1.0 AND dt.TuChon1Diem > 1.0 AND dt.TuChon2Diem > 1.0 AND
             (((dt.TongDiemThi + ISNULL(dhk.DiemTBCacNam,0))/2.0) + ISNULL(dt.DiemUT,0)) >= 5.0 
        THEN N'Đạt' 
        ELSE N'Không đạt' 
    END AS KetQua,
    CASE 
        WHEN (((dt.TongDiemThi + ISNULL(dhk.DiemTBCacNam,0))/2.0) + ISNULL(dt.DiemUT,0)) >= 8.0 
        THEN N'Học sinh giỏi'
        WHEN (((dt.TongDiemThi + ISNULL(dhk.DiemTBCacNam,0))/2.0) + ISNULL(dt.DiemUT,0)) >= 6.5 
        THEN N'Học sinh khá'
        WHEN (((dt.TongDiemThi + ISNULL(dhk.DiemTBCacNam,0))/2.0) + ISNULL(dt.DiemUT,0)) >= 5.0 
        THEN N'Học sinh trung bình'
        ELSE N'Không đạt yêu cầu'
    END AS GhiChu
FROM DM_HocSinh hs
INNER JOIN HocSinh_DiemThi dt ON hs.ID = dt.HS_ID
LEFT JOIN HocSinh_DiemHocKy dhk ON hs.ID = dhk.HS_ID
WHERE hs.ID BETWEEN 1 AND 35;

